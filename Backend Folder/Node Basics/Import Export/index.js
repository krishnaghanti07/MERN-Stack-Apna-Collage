import { sum, mul, sub, divi, g, PI } from './maap.js';

console.log("Sum:", sum(2, 3));     // Output: 5
console.log("Multiplication:", mul(2, 3));  // Output: 6
console.log("Subtraction:", sub(2, 3));     // Output: -1
console.log("Division:", divi(6, 3));       // Output: 2
console.log("Gravitational constant:", g);  // Output: 9.8
console.log("Value of PI:", PI);            // Output: 3.14
